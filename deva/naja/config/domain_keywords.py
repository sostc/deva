def get_domain_keywords(domain: str) -> list:
    """获取指定行业的关键词"""
    return DOMAIN_KEYWORDS.get(domain, [])


def get_all_domains() -> list:
    """获取所有已配置的行业"""
    return list(DOMAIN_KEYWORDS.keys())


def expand_domain_keywords(domains: list) -> dict:
    """
    根据选中的行业扩展关键词

    Args:
        domains: 行业列表，如 ["AI", "芯片"]

    Returns:
        关键词字典
    """
    result = {}

    for domain in domains:
        if domain in DOMAIN_KEYWORDS:
            result[domain] = DOMAIN_KEYWORDS[domain]

    return result


def get_domain_stats(domains: list) -> dict:
    """
    获取行业的关键词统计

    Args:
        domains: 行业列表

    Returns:
        统计信息字典
    """
    stats = {}

    for domain in domains:
        keywords = get_domain_keywords(domain)
        stats[domain] = {
            "count": len(keywords),
            "sample": keywords[:5] if keywords else []
        }

    return stats
