def migrate_diagram_info():
    """迁移策略详解图表信息到数据库"""
    mgr = get_strategy_manager()
    mgr.load_from_db()
    
    all_diagram_info = get_all_diagram_info()
    updated_count = 0
    
    for entry in mgr.list_all():
        strategy_name = entry.name
        
        if strategy_name in all_diagram_info:
            diagram_info = all_diagram_info[strategy_name]
            
            # 更新 metadata 中的 diagram_info
            entry._metadata.diagram_info = diagram_info
            
            # 保存到数据库
            entry.save()
            
            updated_count += 1
            print(f"已更新策略【{strategy_name}】的详解图表信息")
    
    print(f"\n迁移完成！共更新 {updated_count} 个策略的详解图表信息")
    return updated_count


if __name__ == "__main__":
    migrate_diagram_info()
