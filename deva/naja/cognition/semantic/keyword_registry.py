def get_all_keywords() -> List[str]:
    """获取所有叙事关键词（扁平化）"""
    keywords = []
    for topic_keywords in DEFAULT_NARRATIVE_KEYWORDS.values():
        keywords.extend(topic_keywords)
    return list(set(keywords))


def match_keywords(text: str, keyword_dict: Dict[str, List[str]]) -> List[str]:
    """匹配文本中的关键词"""
    text_lower = text.lower()
    matched = []
    for keywords in keyword_dict.values():
        for kw in keywords:
            if kw.lower() in text_lower:
                matched.append(kw)
    return matched
