"""
KeywordRegistry - 统一关键词注册表

认知系统的所有关键词在这里集中管理，避免散落在各处。

关键词分类：
1. NarrativeKeywords - 叙事主题关键词（AI、芯片、新能源等）
2. DynamicsKeywords - 供需动态关键词（供需、技术瓶颈、效率突破）
3. SentimentKeywords - 市场情绪关键词（行情涨跌、市场情绪、舆论热点）
4. SupplyDemandKeywords - 供需关键词（与 Dynamics 有重叠，待整理）
5. MarketNarrativeKeywords - 市场叙事关键词（政策、业绩等）
6. NewsTopicKeywords - 新闻主题关键词（用于新闻聚类）

配置文件：
- 关键词支持从 YAML 配置文件加载
- 配置文件路径：deva/naja/config/keywords/
- 配置文件加载优先级：YAML文件 > 代码默认值

使用方式：
    from deva.naja.cognition.keyword_registry import (
        DEFAULT_NARRATIVE_KEYWORDS,
        DYNAMICS_KEYWORDS,
        SENTIMENT_KEYWORDS,
        MARKET_NARRATIVE_KEYWORDS,
        NEWS_TOPIC_KEYWORDS,
    )
"""

from typing import Dict, List
from pathlib import Path

import yaml


def _get_keywords_config_dir() -> Path:
    """获取关键词配置目录路径"""
    return Path(__file__).parent.parent / "config" / "keywords"


def _load_yaml_keywords(filename: str) -> Dict[str, List[str]]:
    """
    从 YAML 文件加载关键词

    Args:
        filename: YAML 文件名

    Returns:
        Dict[str, List[str]]: 关键词字典，如果加载失败返回空字典
    """
    config_dir = _get_keywords_config_dir()
    filepath = config_dir / filename

    if not filepath.exists():
        return {}

    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            data = yaml.safe_load(f)
            if not data:
                return {}
            # YAML 文件根节点就是分类字典
            return data
    except Exception as e:
        import logging
        logging.getLogger(__name__).warning(f"加载关键词配置文件 {filename} 失败: {e}")
        return {}


def _load_narrative_keywords() -> Dict[str, List[str]]:
    """加载叙事主题关键词"""
    return _load_yaml_keywords("narrative_keywords.yaml")


def _load_dynamics_keywords() -> Dict[str, List[str]]:
    """加载供需动态关键词"""
    return _load_yaml_keywords("dynamics_keywords.yaml")


def _load_sentiment_keywords() -> Dict[str, List[str]]:
    """加载市场情绪关键词"""
    return _load_yaml_keywords("sentiment_keywords.yaml")


